//Additional color definitions
#define YELLOW 0xFFFF00
#define BROWN  0xD2691E
#define DIRT   BROWN
#define GREEN  0x437C17
#define LTBLUE 0xCCFFFF
#define DKBLUE 0x7EFFFF
#define PURPLE 0x800080
#define RED    0xFF0000
#define GRAY   0xA9A9A9
#define BLACK  0x000000
#define WHITE  0xFFFFFF
#define UNDERWATER 0x00B4CC
#define SQUIDLAIR 0x007382
#define ORANGE 0XFFA500
// You can define more hex colors here


//Function to draw images based on characters
// - The function takes an image array and changes the color 
//   labeled by a character to a hex value (ex: 'Y' -> 0xFFF00)
void draw_img(int u, int v, const char* img)
{
    int colors[11*11];
    for (int i = 0; i < 11*11; i++)
    {
        //You can add more characters by defining their hex values above
        if (img[i] == 'R') colors[i] = RED;
        else if (img[i] == 'Y') colors[i] = YELLOW;
        else if (img[i] == 'G') colors[i] = GREEN;
        else if (img[i] == 'D') colors[i] = DIRT;
        else if (img[i] == 'P') colors[i] = PURPLE;
        else if (img[i] == 'L') colors[i] = LTBLUE;
        else if (img[i] == 'K') colors[i] = DKBLUE;
        else if (img[i] == '3') colors[i] = GRAY;
        else if (img[i] == 'B') colors[i] = BLACK;
        else if (img[i] == 'W') colors[i] = WHITE;
        else if (img[i] == 'S') colors[i] = SQUIDLAIR;
        else if (img[i] == 'O') colors[i] = ORANGE;
        else colors[i] = UNDERWATER;
    }
    uLCD.BLIT(u, v, 11, 11, colors);
    wait_us(250); // Recovery time!
}


